#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	char	*tp_s;

	i = 0;
	tp_s = (char *)s;
	if (n == 0)
		return (tp_s);
	while (i < n)
	{
		tp_s[i] = (char)c;
		i++;
	}
	return (s);
}
