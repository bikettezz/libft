#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	unsigned int	i;
	char			*tp;

	i = 0;
	tp = (char *)s;
	if (s && *s && f && *f)
	{
		while (tp[i])
		{
			(*f)(i, &tp[i]);
			i++;
		}
	}
}
